export default (email) => {
    const invalidEmails = email
        .split(',')
        .map(email => email.trim())
        .filter(email => re.test(email) === false)

    if (invalidEmails.length) {
        return `These eamils are invalid: ${invalidEmails}`;
    }
    return;
}; 